    async def complete_content(
        self,
        child_id: UUID,
        content_id: UUID,
        request: CompleteContentRequest,
    ) -> ContentProgress:
        """Complete content and award points."""
        # Check if content exists
        content = await self._get_content(content_id)
        if not content:
                raise ContentNotFoundError(content_id)
        # Check if child has access
        await self._check_access(child_id, content)
        # Get or create progress
        progress = await self._get_or_create_progress(child_id, content_id)
        if progress.completed:
            # Already completed
            return progress
        # Mark as completed
        progress.completed = True
        progress.completed_at = datetime.now(timezone.utc)
        # award points if not already earned
        if progress.points_earned == 0:
            # Calculate bonus
            bonus = content.points_reward + request.points_bonus
            total_points = bonus
            # Check if there's a question session with correct answers
            if request.answers_correct >= 0:
                result = await self.db.execute(
                    select(ContentQuestionSession)
                    .where(
                        ContentQuestionSession.content_id == content_id
                    )
                    .order_by(ContentQuestionSession.created_at.desc())
                    .limit(1)
                )
                question_session = result.scalar_one_or_none()
                if question_session and question_session.correct_answers == request.answers_correct:
                    total_points += question_session.points_earned
            # Award points
            await self.point_service.earn(
                child_id=child_id,
                amount=total_points,
                source_type="content_completion",
                source_id=content_id,
                description=f"Completed: {content.title}",
            )
            progress.points_earned = total_points
        progress.last_updated_at = datetime.now(timezone.utc)
        await self.db.commit()
        return progress

